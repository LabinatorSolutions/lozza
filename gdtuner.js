//{{{  calcErr

function calcErr () {

  //console.log('calc err in');

  var err = 0;
  var num = Vtotal;

  for (var i=0; i < num; i++) {

    var vi  = Vindex(i);
    var epd = epds[vi];

    loz.position(epd.board,epd.turn,epd.rights,epd.ep);

    var pr = epd.prob;
    var ev = loz.evaluate();
    var sg = sigmoid(ev);

    if (isNaN(ev) || isNaN(pr) || isNaN(sg) || sg > 1.0 || pr > 1.0 || sg < 0.0 || pr < 0.0) {
      console.log('nan eek',pr,sg,ev);
      process.exit();
    }

    err += (pr-sg) * (pr-sg);
  }

  //console.log('calc err out');

  return err / num;
}


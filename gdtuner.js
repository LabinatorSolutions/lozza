  //{{{  ttValidate
  
  lozStruct.prototype.ttValidate = function (move) {
  
    const b = this.board;
  
    const frObj = moveFromObj(move);
    const fr    = moveFromSq(move);
  
    if (b[fr] != frObj)
      return false;
  
    const toObj = moveToObj(move);
    const to    = moveToSq(move);
  
    if (b[to] != toObj)
      return false;
  
    return true;
  }
  

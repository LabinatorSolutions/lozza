  //{{{  addQuiet
  
  nodeStruct.prototype.addQuiet = function (move) {
  
    if (moveEq(this.ttMove,move))
      return;
  
    if (moveEq(this.killer,move)) {
      this.killerNum = 1;
      return;
    }
  
    this.quietMoves[this.quietNum++] = move;
  
  }
  

  //{{{  hashCalc
  
  lozStruct.prototype.hashCalc = function () {
  
    this.hLo[0] = 0;
    this.hHi[0] = 0;
  
    this.hHistoryLimit = 0;
    this.hHistoryNext  = 0;
  
    for (let i=0; i < 64; i++) {
      let sq  = B88[i];
      let obj = this.board[sq];
      this.hashObj(obj,sq);
    }
  
    this.hashTurn();
    this.hashRights();
    this.hashEP();
  
    this.hHistoryLo[0] = this.hLo[0];
    this.hHistoryHi[0] = this.hHi[0];
  
    this.hHistoryNext++;
  }
  

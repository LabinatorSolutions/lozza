  //{{{  ttIndex
  
  lozStruct.prototype.ttIndex = function () {
  
    const i = this.hLo[0] & TT_MASK;
  
    if (this.ttFlags[i] && this.hashEq(this.ttLo[i],this.ttHi[i]))
      return i;
    else
      return 0;
  }
  
